package smart.open.data.resource.bean;

public class DatiSensoriBean {
	
	private String idSlot;
	private String startRest;
	private String endRest;
	
	public String getIdSlot() {
		return idSlot;
	}
	public void setIdSlot(String idSlot) {
		this.idSlot = idSlot;
	}
	public String getStartRest() {
		return startRest;
	}
	public void setStartRest(String startRest) {
		this.startRest = startRest;
	}
	public String getEndRest() {
		return endRest;
	}
	public void setEndRest(String endRest) {
		this.endRest = endRest;
	}
	

}
